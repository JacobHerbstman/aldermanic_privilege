# --- 2. Process the String ---

# Split the single string into individual entries based on your separator.
# We use "\n---\n" to mean "a newline, then '---', then a newline"
# `str_trim` cleans up any extra whitespace.
entries <- all_data_string %>%
  str_split("\n---\n") %>%
  unlist() %>%
  str_trim()

# Filter out any empty entries that might result from the split
entries <- entries[entries != "" & !is.na(entries)]

# Create a function to process one entry
parse_entry <- function(entry) {
  # Split the entry string into lines.
  lines <- entry %>%
    str_split("\n") %>%
    unlist()
  
  # The first line is the name
  name <- lines[1] %>% str_trim()
  
  # --- MODIFICATION ---
  # The second line is now the ward number
  ward <- lines[2] %>% str_trim() %>% as.integer()
  
  # --- MODIFICATION ---
  # All other lines (from 3 onwards) are the answer.
  answer <- lines[-(1:2)] %>%
    paste(collapse = "\n") %>%
    str_trim()
  
  # Return a one-row tibble (data frame)
  return(tibble(
    alderman= name,
    ward = ward,
    prerogative_answer = answer
  ))
}


prerogative_data_pasted <- purrr::map_dfr(entries, parse_entry)

write_csv(prerogative_data_pasted, "../output/alderman_prerogative_statements.csv")


